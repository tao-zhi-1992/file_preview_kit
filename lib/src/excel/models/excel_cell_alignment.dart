/// Horizontal alignment for a spreadsheet cell.
enum ExcelHorizontalAlign {
  /// Default / general alignment.
  general,

  /// Left aligned.
  left,

  /// Center aligned.
  center,

  /// Right aligned.
  right,
}

/// Vertical alignment for a spreadsheet cell.
enum ExcelVerticalAlign {
  /// Top aligned.
  top,

  /// Center aligned.
  center,

  /// Bottom aligned.
  bottom,
}
